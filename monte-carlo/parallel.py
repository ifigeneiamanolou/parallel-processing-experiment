import matplotlib.pyplot as plt
import numpy as np
from concurrent.futures import ProcessPoolExecutor
import serial

def parallel_monte_carlo(max_workers, N, n):
    """ Run the random walk multiple times (Monte-Carlo simulation) using a pool of parallel processors
...     Args:
            max_workers (int) : Number of parallel processors
...         N (int): number of simulations
            n (int): number of steps to take in each random walk
...     Returns:
            list[tuple[ndarray, ndarray]] : a list containing x and y coordinates generated by all random walks    
...  """

    results = []
    with ProcessPoolExecutor(max_workers=max_workers) as executor:
        results = list(executor.map(lambda rep: serial.single_random_walk(n, rep), range(N)))

    return results
       

